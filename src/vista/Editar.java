    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = campoId.getText();

            Conexao conexao = new Conexao();
            jdbcTarefa jdbcTarefa = new jdbcTarefa(conexao.abrirConexao());

            campoNome.setText(jdbcTarefa.buscarPorId(id).getNome());
            campoConcluido.setSelected(jdbcTarefa.buscarPorId(id).isConcluido());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(campoId.getText());
        String nome = campoNome.getText();
        boolean concluido = campoConcluido.isSelected();
        
        Tarefa t = new Tarefa(id, concluido, nome);
        
        Conexao conexao = new Conexao();
        jdbcTarefa jdbcTarefa = new jdbcTarefa(conexao.abrirConexao());
        jdbcTarefa.atualizarTarefa(t);
        conexao.fecharConexao();
       
        
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed


